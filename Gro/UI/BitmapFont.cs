    private static void Define(char c, string pattern)
    {
        var rows = pattern.Split(',');
        var bitmap = new byte[rows.Length][];
        for (int i = 0; i < rows.Length; i++)
        {
            bitmap[i] = new byte[rows[i].Length];
            for (int j = 0; j < rows[i].Length; j++)
                bitmap[i][j] = (byte)(rows[i][j] == '1' ? 1 : 0);
        }
        Glyphs[c] = bitmap;
    }

    public static byte[][]? GetGlyph(char c)
    {
        if (Glyphs.TryGetValue(c, out var g)) return g;
        if (char.IsUpper(c) && Glyphs.TryGetValue(char.ToLower(c), out g)) return g;
        if (char.IsLower(c) && Glyphs.TryGetValue(char.ToUpper(c), out g)) return g;
        return null;
    }
